    <h2>Infrastructures</h2>

    <p>
      Séoul dispose de l’un des réseaux de transport les plus modernes, efficaces et accessibles au monde. La ville
      combine technologie, ponctualité et confort pour permettre des déplacements rapides aussi bien dans le centre
      urbain que vers les régions environnantes.</p>

    <h4> 1. Le métro de Séoul</h4>
    <p>Le métro est l’élément central des transports à Séoul. Constitué de nombreuses lignes parfaitement
      interconnectées, il couvre quasiment toute l’agglomération. Les stations sont propres, bien signalées et
      équipées d’ascenseurs, d’écrans d’information en temps réel et de portes palières pour la sécurité. Les trains
      sont fréquents et les trajets restent très abordables. Le métro est également climatisé et dispose du Wi-Fi
      gratuit.</p>

    <h4> 2. Le réseau de bus</h4>
    <p>Les bus complètent le métro et permettent d’atteindre des zones moins desservies. Le système est organisé par
      couleurs :</p>

    <p> Bleu : longues distances entre grands axes,</p>

    <p> Vert : connexions entre quartiers et stations de métro,</p>

    <p>Jaune : circulation dans un même district,</p>

    <p>Rouge : bus interurbains reliant Séoul aux villes voisines.
      Les arrêts indiquent les horaires et les bus disposent généralement d’informations en anglais.</p>

    <h4> 3. Les taxis</h4>
    <p>Les taxis sont nombreux, relativement abordables et disponibles 24h/24. On distingue les taxis standards
      (argentés ou blancs) et les taxis « deluxe » (noirs), plus confortables. La plupart acceptent la carte de
      transport T-money ou les paiements par carte bancaire.</p>

    <h4> 4. Les trains et la connexion interurbaine</h4>
    <p>Séoul est reliée au reste du pays grâce à un réseau ferroviaire performant. Les trains KTX (à grande vitesse)
      permettent de rejoindre Busan, Daegu, Gwangju ou Daejeon en un temps record. Les gares majeures comme Seoul
      Station, Yongsan ou Cheongnyangni sont très modernes et bien connectées au métro.</p>

    <h4> 5. Les aéroports</h4>
    <p>La ville est desservie par deux aéroports :</p>

    <p> Incheon International Airport, l’un des plus réputés au monde, pour la majorité des vols internationaux.</p>

    <p>Gimpo Airport, utilisé pour les vols domestiques et certaines liaisons régionales (Japon, Chine, etc.).
      Les deux aéroports sont facilement accessibles via métro, train express (AREX) ou bus limousine.</p>

    <h4>6. Les modes de transport alternatifs</h4>
    <p> Séoul propose également des vélos en libre-service (Seoul Bike Ddareungi), parfaits pour les trajets courts
      et pour explorer les pistes cyclables le long du fleuve Han. Les trotinettes électriques en libre-service sont
      aussi très répandues.
    </p>
